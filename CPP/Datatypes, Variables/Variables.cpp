#include <iostream>
using namespace std;

int main() {
    // Integer type
    int age = 21;
    cout << "Age: " << age << endl;

    // Floating point type
    float price = 99.99;
    cout << "Price: " << price << endl;

    // Double type
    double pi = 3.14159265;
    cout << "Value of Pi: " << pi << endl;

    // Character type
    char grade = 'A';
    cout << "Grade: " << grade << endl;

    // Boolean type
    bool isPassed = true;
    cout << "Is Passed: " << isPassed << endl; // Prints 1 (true)

    // String type (non-primitive)
    string name = "Vikas";
    cout << "Name: " << name << endl;

    // Changing variable values
    age = 22;
    cout << "Updated Age: " << age << endl;

    return 0;
}
